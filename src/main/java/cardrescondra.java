    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
          AccessToMysql mysql = new AccessToMysql();
          mysql.connectah();
           String col[] = {"Kuitti NRO", "Pöytä NRO","Maksutapa","PVM", "Klo", "Hinta"};     
           DefaultTableModel model = new DefaultTableModel(col, 0);
          String eka = "'" + secondyearbox.getSelectedItem().toString() + "-" + secondmonthbox.getSelectedItem().toString() + "-" + seconddaybox.getSelectedItem().toString() + "'";
          String toka = "'" + yearbox.getSelectedItem().toString() + "-" + monthbox.getSelectedItem().toString() + "-" + daybox.getSelectedItem().toString() + "'";
          
          
          if (cardcash.getSelectedItem().equals("Käteinen")) {
              try {
                  mysql.cardresconrakateinen(model, eka, toka);
              } catch (SQLException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              } catch (ParseException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              }
               tabletti.setModel(model);
               
              try {
                  yhteensa.setText("" + mysql.rescondkateinensumma(eka, toka));
              } catch (SQLException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              }
               
               
          }
                else if (cardcash.getSelectedItem().equals("Kaikki")) {
          try {
              mysql.cardrescondraall(model, eka, toka);
          } catch (SQLException ex) {
              Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
          } catch (ParseException ex) {
            Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
        }
          tabletti.setModel(model);
          
              try {
                  yhteensa.setText("" + mysql.rescondraallsumma(eka, toka));
              } catch (SQLException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              }
          }
          
                else if (cardcash.getSelectedItem().equals("Pankkikortti")) {
              try {
                  mysql.cardresconrapankki(model, eka, toka);
              } catch (SQLException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              } catch (ParseException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              }
              tabletti.setModel(model);
              
              try {
                  yhteensa.setText("" + mysql.rescondkorttisumma(eka, toka));
              } catch (SQLException ex) {
                  Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
              }
                }
          
          
          
          try {
              mysql.closeConnect();
          } catch (SQLException ex) {
              Logger.getLogger(cardrescondra.class.getName()).log(Level.SEVERE, null, ex);
          }
         
      
    }//GEN-LAST:event_jButton1ActionPerformed


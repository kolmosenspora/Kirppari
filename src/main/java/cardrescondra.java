    public void setValues() {
        String days[] = new String[31];
        days[0] = "" + 0 + 1;
        for (int i = 1; i < days.length; i ++) {
            days[i] = "" + (i + 1);
            if (days[i].length() < 2) {
                days[i] = 0 + days[i];
            }
        }
        DefaultComboBoxModel model = new DefaultComboBoxModel(days);
        daybox.setModel(model);
        
        String months[] = new String[12];
        months[0] = "" + 0 + 1;
               for (int i = 1; i < months.length; i ++) {
            months[i] = "" + (i + 1);
            if (months[i].length() < 2) {
                months[i] = 0 + months[i];
            }
            
        }
        DefaultComboBoxModel modelmm = new DefaultComboBoxModel(months);
        
        monthbox.setModel(modelmm);
        
        String years[] = new String[20]; 
        
                       for (int i = 0; i < years.length; i ++) {
            years[i] = "" + (2019 + i);
        }
        DefaultComboBoxModel modelyyyy = new DefaultComboBoxModel(years);
        
        yearbox.setModel(modelyyyy);
        
         DefaultComboBoxModel modelseconddd = new DefaultComboBoxModel(days);
         DefaultComboBoxModel modelmmsecond = new DefaultComboBoxModel(months);
         DefaultComboBoxModel modelyyyysecond = new DefaultComboBoxModel(years);
         
         seconddaybox.setModel(modelseconddd);
         secondmonthbox.setModel(modelmmsecond);
         secondyearbox.setModel(modelyyyysecond);
         
         String[] pannkkivaikateinen = new String[3];
         pannkkivaikateinen[0] = "Kaikki";
         pannkkivaikateinen[1] = "Käteinen";
         pannkkivaikateinen[2] = "Pankkikortti";
         DefaultComboBoxModel kateinenvaipankki = new DefaultComboBoxModel(pannkkivaikateinen);
         cardcash.setModel(kateinenvaipankki);
    }

